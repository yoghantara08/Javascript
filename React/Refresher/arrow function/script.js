// Function biasa
function printMyName(name) {
  console.log(name);
}

printMyName("Gustut");

// Arrow Function
const consoleMyName = (name) => {
  console.log(name);
};
consoleMyName("Yoghantara");

const sayHello = () => {
  console.log("Hello");
};
sayHello();

const multiply = (number) => {
  return number * 2;
};
console.log(multiply(5));

const tambah = (a, b) => a + b;
console.log(tambah(50, 20));
