// Arrow Function merupakan bentuk lain yang lebih ringkas dari Function Expression

// Function Expression
let tampilPesan1 = function (nama) {
  console.log(`Halo, ${nama}`);
};

tampilPesan1('Gustut');

// Arrow Function
// function () menjadi () =>
let tampilPesan2 = (nama) => {
  console.log(`Halo, ${nama}`);
};

tampilPesan2('Yoghantara');
